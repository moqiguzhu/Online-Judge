# 有点艺术
# 问题规模小且思路复杂的问题，直接把答案全部算出来


class Solution:
    def tilingRectangle(self, n: int, m: int) -> int:
        if n < m:
            n, m = m, n
        ans = [
            [1],
            [2, 1],
            [3, 3, 1],
            [4, 2, 4, 1],
            [5, 4, 4, 5, 1],
            [6, 3, 2, 3, 5, 1],
            [7, 5, 5, 5, 5, 5, 1],
            [8, 4, 5, 2, 5, 4, 7, 1],
            [9, 6, 3, 6, 6, 3, 6, 7, 1],
            [10, 5, 6, 4, 2, 4, 6, 5, 6, 1],
            [11, 7, 6, 6, 6, 6, 6, 6, 7, 6, 1],
            [12, 6, 4, 3, 6, 2, 6, 3, 4, 5, 7, 1],
            [13, 8, 7, 7, 6, 6, 6, 6, 7, 7, 6, 7, 1]
        ]
        return ans[n-1][m-1]
